<script>
    document.addEventListener('DOMContentLoaded', function () {
        document.querySelectorAll('.leadership-readmore').forEach(function (button) {
            button.addEventListener('click', function () {
                var card = button.closest('.leadership-member-card');
                var modal = document.getElementById('leadershipModal');
                var modalTitle = document.getElementById('leadershipModalTitle');
                var modalRole = document.getElementById('leadershipModalRole');
                var modalBody = document.getElementById('leadershipModalBody');

                modalTitle.textContent = card.querySelector('h3').textContent;
                modalRole.textContent = card.querySelector('.info-chip').textContent;
                modalBody.innerHTML = card.querySelector('.leadership-modal-content').innerHTML;
                modal.classList.add('is-open');
                modal.setAttribute('aria-hidden', 'false');
                document.body.classList.add('modal-open');
            });
        });

        document.querySelectorAll('[data-modal-close]').forEach(function (button) {
            button.addEventListener('click', function () {
                var modal = document.getElementById('leadershipModal');
                modal.classList.remove('is-open');
                modal.setAttribute('aria-hidden', 'true');
                document.body.classList.remove('modal-open');
            });
        });

        document.addEventListener('keydown', function (event) {
            if (event.key === 'Escape') {
                var modal = document.getElementById('leadershipModal');
                modal.classList.remove('is-open');
                modal.setAttribute('aria-hidden', 'true');
                document.body.classList.remove('modal-open');
            }
        });

        var leadershipSlider = document.querySelector('.leadership-card-grid');
        var leadershipPrev = document.querySelector('.leadership-slider-prev');
        var leadershipNext = document.querySelector('.leadership-slider-next');

        if (leadershipSlider && leadershipPrev && leadershipNext) {
            var getSlideDistance = function () {
                var card = leadershipSlider.querySelector('.leadership-member-card');
                var gap = parseInt(window.getComputedStyle(leadershipSlider).columnGap, 10) || 24;
                return card ? card.offsetWidth + gap : leadershipSlider.clientWidth;
            };

            leadershipPrev.addEventListener('click', function () {
                leadershipSlider.scrollBy({
                    left: -getSlideDistance(),
                    behavior: 'smooth'
                });
            });

            leadershipNext.addEventListener('click', function () {
                leadershipSlider.scrollBy({
                    left: getSlideDistance(),
                    behavior: 'smooth'
                });
            });
        }
    });
</script>

